#include "libft.h"

int		ft_get_size(const char *s, char c)
{
	int	i;
	int size;

	size = 0;
	i = -1;
	while (s[++i])
		if ((s[i + 1] == c || s[i + 1] == '\0') && s[i] != c)
			size++;
	return (size);
}
